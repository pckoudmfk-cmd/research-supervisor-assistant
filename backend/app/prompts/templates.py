WORK_TYPE_RU = {
    "article": "научная статья",
    "thesis": "тезисы доклада",
    "coursework": "курсовая работа",
    "vkr": "выпускная квалификационная работа (ВКР)",
    "practical": "практический проект",
}

LEVEL_RU = {
    "spo": "СПО (среднее профессиональное образование)",
    "vuz": "ВУЗ (высшее образование)",
}


def ktp_parse_prompt(text: str) -> str:
    return f"""Ты — ассистент преподавателя. Из текста календарно-тематического плана (КТП) или рабочей программы извлеки список учебных тем.

Текст КТП:
{text[:4000]}

Требования:
- Выдели только конкретные учебные темы (не разделы, не номера занятий)
- Каждая тема — отдельная строка
- Без нумерации, без лишних символов
- Если тем больше 20, выбери наиболее содержательные
- Формулировки сохраняй близкими к оригиналу

Выведи только список тем, без пояснений."""


def topic_formulation_prompt(
    ktp_topic: str, work_type: str, level: str, direction: str, subject_area: str
) -> str:
    wt = WORK_TYPE_RU.get(work_type, work_type)
    lvl = LEVEL_RU.get(level, level)
    return f"""Ты — опытный научный руководитель. На основе учебной темы из КТП сформулируй полноценную научную тему.

Исходная тема из КТП: «{ktp_topic}»
Тип работы: {wt}
Уровень: {lvl}
Направление подготовки: {direction}
Предметная область: {subject_area}

Верни ответ строго в формате JSON:
{{
  "topic": "Академическая формулировка научной темы",
  "relevance": "Актуальность исследования в 3-4 предложениях. Почему эта тема важна сейчас, какие проблемы решает.",
  "novelty": "Научная новизна в 2-3 предложениях. Что нового вносит данное исследование."
}}

Требования:
- Тема должна быть конкретной, содержать объект и предмет исследования
- Актуальность — со ссылкой на современные тенденции и практические проблемы
- Новизна — чётко обозначает вклад работы
- Всё соответствует уровню {lvl} и типу «{wt}»
- Только валидный JSON, без дополнительного текста"""


def plan_generation_prompt(topic: str, work_type: str, level: str) -> str:
    wt = WORK_TYPE_RU.get(work_type, work_type)
    lvl = LEVEL_RU.get(level, level)
    return f"""Ты — опытный научный руководитель. Разработай подробный план исследования.

Тема: «{topic}»
Тип работы: {wt}
Уровень: {lvl}

Верни ответ строго в формате JSON:
{{
  "goal": "Цель исследования — одно чёткое предложение",
  "objectives": [
    "Задача 1",
    "Задача 2",
    "Задача 3",
    "Задача 4",
    "Задача 5"
  ],
  "keywords": ["ключевое слово 1", "ключевое слово 2", "ключевое слово 3"],
  "chapters": [
    {{
      "number": 1,
      "title": "Название главы",
      "description": "Краткое описание содержания главы (1-2 предложения)"
    }}
  ]
}}

Требования:
- Цель: начинается с глагола (Исследовать, Разработать, Проанализировать...)
- Задачи: 5-7 штук, каждая начинается с глагола в инфинитиве
- Ключевые слова: 8-12 слов/словосочетаний, отражающих суть работы
- Главы: введение, 2-3 основные главы, заключение (4-5 всего)
- Объём и структура соответствуют {wt} уровня {lvl}
- Только валидный JSON, без дополнительного текста"""
